#!/bin/bash 

# PATH: list of folders that the shell will search for executable files
echo "------------------------------------------------------"
echo $PATH
echo "------------------------------------------------------"

# HOME: Used to store the absolute path to the current user's home directory.

echo "------------------------------------------------------"
echo $HOME
echo "------------------------------------------------------"

# USER: Contains the username of the current user.
echo "------------------------------------------------------"
echo $USER
echo "------------------------------------------------------"

# HOSTNAME: the name of the current computer

exit 0
